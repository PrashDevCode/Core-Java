public class WhileLoop {
    public static void main(String[] args) {

        int t=1;
        while(t<=5) {

            System.out.println("Java " + t);
            int s=1;
            while(s<=2) {
                
                System.out.println("WHile Loop " + s);
                s++;
            }
            
            t++;
        }
        
    }
}
