public class WhileL {
    public static void main(String[] args) {
        int i;
        i=1;
        while(i<=10) {
            System.out.println(i);
            i++;
        }

        i=2;
        while(i<=20) {
            System.out.println(i);
            i=i+2;
        } 

        i=10;
        while(i>=1) {
            System.out.println(i);
            i--;
        } 

        i=20;
        while(i>=1) {
            System.out.println(i);
            i=i-2;
        } 

        i=5;
        while(i<=50) {
            System.out.println(i);
            i=i+5;
        }
    }
}
