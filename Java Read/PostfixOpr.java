public class PostfixOpr {
    public static void main(String[] args) {
        int a=10; 
        // Postfix Increment/Decrement Operator 
        // first perform and then evaluate 
        System.out.println(a++);
        System.out.println(a);
        System.out.println(a--);
        System.out.println(a);
    }
}
